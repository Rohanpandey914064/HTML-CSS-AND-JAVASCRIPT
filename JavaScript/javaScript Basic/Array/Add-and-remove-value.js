let students = ["rohan" , "tom" , "jarry"];
console.log(students);

//to add element at end 
students.push("pawan");
console.log(students);

//to add element at beginning
students.unshift("raj");
console.log(students);


//to remove element at end
students.pop();
console.log(students);

//to remove element at beginning
students.shift();
console.log(students);
