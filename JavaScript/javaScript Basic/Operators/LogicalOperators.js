//And operator
console.log(true && true); //Output - true
console.log(true && false); //Output - false
console.log(false && false); //Output - false


//or operator
console.log(true || true); //Output - true
console.log(true || false); //Output - true
console.log(false || false); //Output - false

//not operator
console.log(!true); //Output - false
console.log(!false); //Output - true

